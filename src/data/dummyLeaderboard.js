// Function to get paginated leaderboard
export const getPaginatedLeaderboard = (leaderboard, page = 1, itemsPerPage = 10) => {
    const startIndex = (page - 1) * itemsPerPage;
    const endIndex = startIndex + itemsPerPage;
    return {
        data: leaderboard.slice(startIndex, endIndex),
        totalPages: Math.ceil(leaderboard.length / itemsPerPage),
        currentPage: page,
        totalItems: leaderboard.length
    };
};

// Function to filter leaderboard
export const filterLeaderboard = (leaderboard, filters = {}) => {
    let filtered = [...leaderboard];

    if (filters.topic && filters.topic !== 'All') {
        filtered = filtered.filter(entry => entry.topic === filters.topic);
    }

    if (filters.difficulty && filters.difficulty !== 'All') {
        filtered = filtered.filter(entry => entry.difficulty === filters.difficulty);
    }

    if (filters.timePeriod) {
        const now = new Date();
        const cutoffDate = new Date();

        switch (filters.timePeriod) {
            case 'today':
                cutoffDate.setHours(0, 0, 0, 0);
                break;
            case 'week':
                cutoffDate.setDate(now.getDate() - 7);
                break;
            case 'month':
                cutoffDate.setMonth(now.getMonth() - 1);
                break;
            default:
                break;
        }

        if (filters.timePeriod !== 'all') {
            filtered = filtered.filter(entry => new Date(entry.timestamp) >= cutoffDate);
        }
    }

    return filtered;
};
